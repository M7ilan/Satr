function date_format(date) {
	let date1 = date.split("/");
	let date2 = date.split("/");
	let date3 = date.split("/");
	return date1[0] + "/" + date1[1] + "/" + date1[2] + " | " + date2[0] + "-" + date2[1] + "-" + date2[2] + " | " + date3[1] + "/" + date3[2] + "/" + date3[0];
}
